/**
 * @sumary Calcula la suma de dos numeros
 * @description Esta funcion sirve para sumar dos numeros que toma como parametros
 * @param {number} a Primer sumando
 * @param {number} b Segundo sumando
 * @returns {number} Devuelve la suma de los dos numeros
 */
function suma(a, b){
    return a + b;
}